import Link from "next/link";
import { notFound } from "next/navigation";
import { VideoPlayer } from "@/components/library/VideoPlayer";
import { ArrowLeftIcon, SparkleIcon } from "@/components/icons";
import { requireMember } from "@/lib/current-member";
import { generateMuxPlaybackToken, isMuxSigningConfigured } from "@/lib/mux";
import { createServiceClient } from "@/lib/supabase/admin";
import { createClient } from "@/lib/supabase/server";
import { isSupabaseConfigured } from "@/lib/supabase/config";
import { getVideo } from "@/lib/videos/queries";
import { VideoNotesEditor } from "@/components/library/VideoNotesEditor";

export const dynamic = "force-dynamic";

export default async function VideoDetailPage({
  params,
}: {
  params: { id: string };
}) {
  const member = await requireMember();
  const video = await getVideo(params.id, member.tier);
  if (!video) notFound();

  // Self-heal: a freshly imported recording has a Mux asset but no playback
  // id until Mux finishes encoding. Don't make the viewer wait for the next
  // cron pass — ask Mux now and attach the playback id the moment it's ready.
  if (
    !video.muxPlaybackId &&
    isSupabaseConfigured() &&
    process.env.SUPABASE_SERVICE_ROLE_KEY
  ) {
    const service = createServiceClient();
    const { data: row } = await service
      .from("videos")
      .select("mux_asset_id")
      .eq("id", video.id)
      .maybeSingle();
    if (row?.mux_asset_id) {
      try {
        const { getMuxAsset } = await import("@/lib/mux");
        const asset = await getMuxAsset(row.mux_asset_id as string);
        const playbackId = asset.playback_ids?.[0]?.id ?? null;
        if (asset.status === "ready" && playbackId) {
          await service
            .from("videos")
            .update({
              mux_playback_id: playbackId,
              ...(asset.duration
                ? { duration_sec: Math.round(asset.duration) }
                : {}),
            })
            .eq("id", video.id);
          video.muxPlaybackId = playbackId;
        }
      } catch {
        // Still encoding (or Mux unreachable) — the processing state shows.
      }
    }
  }

  // The viewer's private note on this recording (owner-only via RLS).
  // A session recording CARRIES OVER the notes the member took during the
  // live session — until they save a library-specific note, they see the
  // notes they wrote in the room instead of an empty box.
  let initialNote = "";
  if (isSupabaseConfigured()) {
    const supabase = createClient();
    const [{ data: note }, sessionNote] = await Promise.all([
      supabase
        .from("video_notes")
        .select("body")
        .eq("video_id", video.id)
        .maybeSingle(),
      video.sessionId
        ? supabase
            .from("session_notes")
            .select("body")
            .eq("session_id", video.sessionId)
            .maybeSingle()
        : Promise.resolve({ data: null }),
    ]);
    initialNote =
      note?.body ??
      (sessionNote.data as { body?: string } | null)?.body ??
      "";
  }

  // Signed playback token so recording URLs can't leave the portal.
  const playbackToken =
    video.muxPlaybackId && isMuxSigningConfigured()
      ? generateMuxPlaybackToken(video.muxPlaybackId)
      : null;

  const ai = video.aiSummary;

  return (
    <div className="video-detail-wrap">
      <Link href="/library" className="sess-back">
        <ArrowLeftIcon size={12} /> Library
      </Link>

      <VideoPlayer
        videoId={video.id}
        playbackId={video.muxPlaybackId}
        playbackToken={playbackToken}
        title={video.title}
      />

      <h1 className="video-detail-title">{video.title}</h1>
      <div className="video-detail-meta">
        <strong>{video.speakerName}</strong> · {video.category} ·{" "}
        {video.durationLabel} · {video.dateLabel}
      </div>

      <div className="two-col">
        <div className="card">
          <div className="card-header">
            <h3>AI Summary</h3>
          </div>
          <div style={{ padding: 18 }}>
            {!ai ? (
              <div className="sess-empty-note">
                The AI summary is generated after the recording is processed.
              </div>
            ) : (
              <div>
                <div className="ai-header">
                  <SparkleIcon size={16} />
                  <div className="ai-header-text">
                    <strong>Generated by Momentum+ AI</strong>
                  </div>
                </div>
                {ai.highlights && (
                  <p className="sess-desc" style={{ marginBottom: 14 }}>
                    {ai.highlights}
                  </p>
                )}
                {ai.takeaways.length > 0 && (
                  <div className="ai-section">
                    <div className="ai-section-title">Key Takeaways</div>
                    {ai.takeaways.map((t) => (
                      <div className="ai-takeaway" key={t}>
                        {t}
                      </div>
                    ))}
                  </div>
                )}
                {ai.quotes.length > 0 && (
                  <div className="ai-section">
                    <div className="ai-section-title">Notable Quotes</div>
                    {ai.quotes.map((q) => (
                      <div className="ai-quote" key={q}>
                        &ldquo;{q}&rdquo;
                      </div>
                    ))}
                  </div>
                )}
                {ai.actionItems.length > 0 && (
                  <div className="ai-section">
                    <div className="ai-section-title">Action Items</div>
                    {ai.actionItems.map((a, i) => (
                      <div className="ai-action" key={a}>
                        <span className="ai-num">{i + 1}</span>
                        {a}
                      </div>
                    ))}
                  </div>
                )}
              </div>
            )}
          </div>
        </div>

        <div className="card">
          <div className="card-header">
            <h3>My Notes</h3>
          </div>
          <div style={{ padding: 18 }}>
            <VideoNotesEditor videoId={video.id} initialNote={initialNote} />
          </div>
        </div>
      </div>
    </div>
  );
}
